// The single place the app reads the wall clock. Engines stay pure by taking
// `today` as a parameter; this supplies it from the user's local calendar.

export function todayISO(): string {
  const d = new Date();
  const y = d.getFullYear();
  const m = String(d.getMonth() + 1).padStart(2, '0');
  const day = String(d.getDate()).padStart(2, '0');
  return `${y}-${m}-${day}`;
}
